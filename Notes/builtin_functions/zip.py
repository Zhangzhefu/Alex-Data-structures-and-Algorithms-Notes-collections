# The zip() function in Python is used to combine two or more iterables (like lists, tuples, or strings) into a
# single iterable of tuples. Each tuple contains elements from the corresponding positions of the input iterables.
#
# Basic Syntax

# zip(iterable1, iterable2, ...)
# iterable1, iterable2, ...: The input iterables you want to combine.
# The function stops when the shortest input iterable is exhausted.
#
# Key Points
# Combines elements from the input iterables into tuples.
# Stops at the shortest iterable, discarding extra elements in the longer ones.
# Returns a zip object, which is an iterator. You can convert it to a list, tuple, or iterate directly.
