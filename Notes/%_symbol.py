# % symbol
#
# this is the modulo symbol, this returns the remainder of an division

print(5 % 2)
# -- this will return with the value of 1, because 2 goes in 5 two times, and it is left with remainder of 1.


# It can also be used as special formatting, just like print(f"")
name = "Alex"
print("hi %s" % name)
# -- this will return with "hi Alex" as an output.
